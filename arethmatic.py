
# include add, subtract,  multiply & divide in this area

def add(num1, num2):
    # adding together
    return num1 + num2


def subtract(num1, num2):
    # subtracting
    return num1 - num2


def multiply(num1, num2):
    # multiplying
    return num1*num2


def divide(num1, num2):
    # dividing
    return num1 / num2


