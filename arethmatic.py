def adding(num1, num2):
    # adding together
    num3 = num1 + num2
    return num3


def subtracting(num1, num2):
    # subtracting
    num3 = num1 - num2
    return num3


def multiplying(num1, num2):
    # multiplying
    num3 = num1*num2
    return num3
