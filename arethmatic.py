
# include add, subtract,  multiply & divide in this area

def add(num1, num2):
    # adding together
    num3 = num1 + num2
    return num3


def subtract(num1, num2):
    # subtracting
    num3 = num1 - num2
    return num3


def multiply(num1, num2):
    # multiplying
    num3 = num1*num2
    return num3


def divide(num1, num2):
    # dividing
    num3 = num1 / num2
    return num3


print("adding 3 and 2:")
print(add(3, 2))
print("subtracting 3 and 2:")
print(subtract(3, 2))
print("multiplying 3 and 2:")
print(multiply(3, 2))
print("dividing 3 and 2:")
print(divide(3, 2))
